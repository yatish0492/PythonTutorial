'''
Arithmetic Operators in Python
'''


# Following will give 2.5
print(10/4)

# Integer Division or Floor Division '//'
# Following will give 2 instead of 2.5.
print(10//4)

# Power of Operator '**'
# Following will give you 2 power 3 which is '8'
print(2**3)


